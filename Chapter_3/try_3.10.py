cities = ['Porto', 'Lisboa', 'Viseu', 'Vigo', 'Wien']
print(cities)
print(len(cities))

#Reverse alphabetical order
cities.sort(reverse=True)
print(cities)

#Sorting a list temporarily with the sorted() function
print("\nHere is the sorted list:")
print(sorted(cities))


#Printing a list in Reverse Order
print(cities)
cities.reverse()




